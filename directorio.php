	<script>
		document.addEventListener("DOMContentLoaded", function() {
			const tbResponsables = document.getElementById('tb-respondables');

			function render() {


				fetch("./src/json/responsables.json")
					.then(response => {
						return response.json();
					})
					.then(jsondata => {

						const data = [...jsondata]
						console.log(data);

						for (let item of data) {


							let row = `
										<tr style="font-size: 12px;">
											<td scope="col">${item.id}</td>
											<td scope="col">${item.cargo.toUpperCase()}</td>
											<td scope="col">${item.nombres}</td>
											<td scope="col">${item.correo == undefined ? "-" : item.correo}</td>
											<td scope="col">${item.celular}</td>
										</tr>
									`;
							tbResponsables.innerHTML += row;


						}

					})
					.catch(error => console.log(error));
			}

			render();
		})
